num1 = int(input())
num2 = int(input())

soma = num1 + num2
subtração = num1 - num2
multiplicação = num1 * num2
divisao = num1 // num2
porcetagem = num1 % num2

print(f"{num1} + {num2} = {soma:.0f}")
print(f"{num1} - {num2} = {subtração:.0f}")
print(f"{num1} * {num2} = {multiplicação:.0f}")
print(f"{num1} // {num2} = {divisao:.0f}") # resultado arredondado
print(f"{num1} % {num2} = {porcetagem:.0f}")
