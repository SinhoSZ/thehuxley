x = int(input())
y = int(input())

# 0 = falso
# 1= verdadeiro
if x > y: # x é maior que y
    print(1)
else:
    print(0)

if x == y: # x é igual a y
    print(1)
else:
    print(0)

if x < y: # x é menor que y
    print(1)
else:
    print(0)

if x != y: # x é diferente de y
    print(1)
else:
    print(0)

if x >= y: # x é maior ou igual a y
    print(1)
else:
    print(0)

if x <= y: # x é menor ou igual a y
    print(1)
else:
    print(0)
